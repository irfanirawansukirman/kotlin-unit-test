package com.irfanirawansukirman.belajarunittest

open class Calculator {

    fun add(number1: Int, number2: Int) = number1 + number2

    fun divider(number1: Int, number2: Int) = number1 / number2

    fun subtract(number1: Int, number2: Int) = number1 - number2

    fun multiply(number1: Int, number2: Int) = number1 * number2

    fun mod(number: Int) = number % 2 == 0

}